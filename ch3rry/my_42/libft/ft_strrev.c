#include "libft.h"

void	ft_strrev(char *str)
{
	size_t	len;
	size_t	i;

	if (str)
	{
		i = 0;
		len = ft_strlen(str) - 1;
		while (i <= len)
				ft_swap(&str[i++], &str[len--]);
	}
}