#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*buff;

	if ((buff = dst) && src)
	{
		while (*src)
			*(buff++) = *(src++);
		*buff = '\0';
	}
	return (dst);
}
