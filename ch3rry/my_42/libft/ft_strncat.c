#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*buff;

	if (s1 && s2 && (n != 0))
	{
		buff = s1;
		while (*buff)
			buff++;
		while (n--)
			*(buff++) = *(s2++);
		*buff = '\0';
	}
	return (s1);
}
