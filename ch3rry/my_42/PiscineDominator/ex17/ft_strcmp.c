int	ft_strcmp(char *s1, char *s2)
{
	char	*p_s1;
	char	*p_s2;

	p_s1 = s1;
	p_s2 = s2;
	while (*p_s1 && *p_s1 == *p_s2)
	{
		p_s1++;
		p_s2++;
	}
	return (*p_s1 - *p_s2);
}
