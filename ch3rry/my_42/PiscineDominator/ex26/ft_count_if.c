int	ft_count_if(char **tab, int (*f)(char*))
{
	int	i;
	int n;

	i = 0;
	n = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			n++;
		i++;
	}
	return (n);
}
