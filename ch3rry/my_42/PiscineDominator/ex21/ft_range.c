#include <stdlib.h>

int	*ft_range(int min, int max)
{
	int i;
	int	*range;
	int diff;

	diff = max - min;
	if (max <= min)
		return (NULL);
	range = (int*)malloc(sizeof(int) * diff);
	if (!range)
		return (NULL);
	i = 0;
	while (min < max)
	{
		range[i] = min;
		i++;
		min++;
	}
	return (range);
}
