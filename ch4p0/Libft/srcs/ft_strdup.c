#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char *scp;
	char *s2;

	scp = (char*)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if ((s2 = scp))
	{
		while (*(s1) != '\0')
			*(s2++) = *(s1++);
		*(s2) = '\0';
	}
	return (scp);
}
