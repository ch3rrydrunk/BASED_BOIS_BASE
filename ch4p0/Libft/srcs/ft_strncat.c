#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	char *buf;

	buf = s1;
	while (*(buf) != '\0')
		buf++;
	while (n-- && *(s2) != '\0')
		*(buf++) = *(s2++);
	*(buf++) = '\0';
	return (s1);
}
