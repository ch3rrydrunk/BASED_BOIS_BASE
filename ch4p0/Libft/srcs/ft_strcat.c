#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	char *buf;

	buf = s1;
	while (*(buf) != '\0')
		buf++;
	while (*(s2) != '\0')
		*(buf++) = *(s2++);
	*(buf) = '\0';
	return (s1);
}
